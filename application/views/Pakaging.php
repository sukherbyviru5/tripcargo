                      <h1>Pakaging Barang</h1>
                      <div style="margin-top: -40px;">
                        Trip Cargo dalam mengirim barang selalu mengutamakan keamanaan barang, <b><i style="color: red;">"kemasan apa sudah aman"</i></b><br> 
                        seperti pack, master pack, plastik pembungkus, bubble wrap, wrapping, box teripek, peti Kayu atau kemasan yang lebih kuwat karna beban berat harus packing yang kokoh.<br>
                        Kemasan yang menggunakan keamaan lebih atau kemasan yang ringan, kami sesuaikan dengan perangkat yang akan di packing.<br>                 
                        Kami menyediakan packing ringan seperti karung, Mailer Bags (plastik dokument), karton, plastik wrapping, sterefom dan perlengkapan kemasan lainnya.<br>
                        Setiap barang yang kami paking sebelumnya sudah kami pastikan dulu untuk di cek dengan baik untuk menjaga keamaanya dalam perjalaan dan pengankatan atau di bongkar muat barang.<br>
                        Dengan pertimbangan untuk bongkar muat barang yang kami gunakan seperti alat berat (forklif).<br>
                        Contoh barang-barang sperpart atau mesin-mesin, artinya packing yang aman dan harus kokoh misal barang juga harus dipeti box kayu.<Br>
                        Barang yang akan dipacking kayu atau bok kayu kami selalu mengedepankan tingkat kerawanaannya barang misal barang cairan dan barang pacah belah harus dilapisi bubble wrap lalu dibungkus carton dan di peti kayu.<br>
                        <b><i style="color: kuning;">"Jangan menyesal dikemudian pastikan barang-barang anda dipacking dengan aman jangan sampai rusak atau pecah karna packing yang tidak standar".</i></b><br>
