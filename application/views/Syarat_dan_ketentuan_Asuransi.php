<div style="margin-top: -20px;">
<br>Asuransi barang itu penting ini adalah layanan tambahan menghindari kerugian besar asuransi di kelola oleh pihak ketiga yaitu (perusahaan asuransi) Trip Cargo mejalin kerjasama dengan pihak asuransi untuk  menjamin serta  melindungi barang dari resiko rusak dan hilang selama perjalanan.
<br>Dalam hal ini Trip Cargo berkerja sama dengan PT. Asuransi Ramayana Syariah.
<br>Syarat dan ketentuan asuransi, serta persetujuan claim adalah sepenuhnya wewenang pihak asuransi dan tunduk kepada MOP (Marine Open Policy) dan peraturan perundang-undangan negara republik Indonesia. 
<br>Trip Cargo membantu pengajuan claim kepada  pihak asuransi, setelah pemilik barang melengkapi dokumen yang dibutuhkan, yaitu :
<ul>
<li>Berita acara kerusakan/kehilangan yang ditanda tangani oleh penerima dan diketahui oleh pengantar / pengangkut di lokasi tujuan (dilokasi barang diterima)</li>
<li>Faktur/kuitansi asli bukti pembelian barang, untuk barang baru</li>
</ul>
<div class="colss-md-12">
<b>Jangan sampai menyesal perhatikan keselamatan barang kiriman akibat pengepakan yang tidak sempurna atau faktor cuaca sehingga menimbulkan kerugian karna isi kiriman menjadi rusak basah dan hilang, sepenuhnya menjadi resiko pengirim dan penerima maka dari itu diperlukan asuransi.</b>
</div>

<br>
<br>
<b>Barang yang akan diasuransikan harus memenuhi persyaratan kemasan standar, kuat dan melindungi, yaitu :</b>
<ul>
<li>Packing kayu : untuk kiriman elektronik, cairan dan barang mudah hancur.</li>
<li>Packing karung : untuk textile, barang cetakan berat, suku cadang dan lain lain.</li>
<li>Packing dus + (wrap / buble plastic) : untuk kiriman relative ringan, rendah resiko. Seperti makanan, snack, kosmetik  dan asesoris.</li>
<li>Semua jenis barang kiriman baik baru (new goods) atau bekas (second goods) dapat diasuransikan kecuali barang pecah belah, barang terbuat dari ceramic, gerabah, barang seni, barang berbahan kaca seperti botol kaca, kaca mobil, kaca tv. Semua barang yg disebutkan diatas tidak dicover asuransi.</li>
</ul>

<b>Besaran Premi</b>
<ul>
<li>Barang umum 0,4% dari nilai barang, minimum charge Rp. 75.000</li>
<li>Personal Effect ( barang pindahan) 1% dari nilai barang, minimum charge Rp. 100.000</li>
<li>Motor 0,4% dari nilai motor, minimum charge Rp. 75.000</li>
<li>Mobil 0,4% dari nilai mobil, minimum charge Rp. 150.000</li>
</ul>
<b>Ketentuan penggantian</b>
<ul>
<li>Penggantian asuransi 90% dari nilai pengajuan claim dengan potongan administrasi claim Rp 300.000</li>
<li>Penggantian dibawah Rp 300.000 tidak diproses perusahaan asuransi.</li>
</ul>

<div>
    <br><br>
<div class="colss-md-12">    
<b>Apakah produk asuransi ini?</b>
<br>
Secara umum jenis asuransi ini memberikan perlindungan atas potensi kerugian keuangan Tertanggung terhadap risiko-risiko kerugian dan kerusakan barang-barang selama barang-barang tersebut menjalani pengangkutan atau pengiriman dengan alat angkut utama darat, laut maupun udara.<br>
Produk asuransi pengangkutan mencakup antara lain : Asuransi Pengangkutan Darat, Asuransi Pengangkutan Laut dan Asuransi Pengangkutan Udara.
<br><br>
<b>Apa yang menjadi obyek pertanggungan asuransi ini?</b>
<br>
Secara garis besar barang-barang yang dapat dipertanggungkan adalah segala macam jenis barang atau komoditi dengan bentuk padat, cair maupun gas dan diangkut dalam kontainer maupun non kontainer.
<br><br>
<b>Siapa saja yang memerlukan produk ini?</b>
<br>
Setiap badan usaha swasta, pemerintah atau individu yang memiliki kepentingan atas obyek yang dipertanggungkan antara lain : eksportir, importir, perusahaan pengangkutan (forwarding), perusahaan jasa kurir, shipowners, dan pihak-pihak lainnya yang berkepentingan.
<br><br>
<b>Bagaimana perhitungan preminya?</b>
<br>
Premi dihitung berdasarkan perkalian antara nilai obyek yang dipertanggungkan (termasuk biaya freight dan imaginary profit) dengan rate premi yang ditetapkan dengan memperhatikan antara lain : jenis, sifat dan kemasan barang, usia alat angkut, rute pengangkutan dan faktor-faktor lainnya.
<br><br>
<b>Bagaimana proses pengajuan klaim?</b>
<br>
Dalam hal terjadi klaim, Tertanggung dapat melakukan langkah-langkah sebagai berikut :
Hubungi Kantor kami yang terdekat dengan rentang waktu yang wajar sejak diketahui kejadian klaim.
Selanjutnya petugas klaim kami akan membantu proses klaim ini. 
</div>
    <br><br>

<b>Risiko Yang    Dijamin (dicover oleh perusahaan asuransi)</b>
 <br><b>ASURANSI PENGANGKUTAN LAUT</b>
<ul>
Kegunaan
<br>Pertanggungan/polis ini memberikan jaminan untuk pengangkutan barang dengan rute dari warehouse/port to warehouse.
</ul>
<b>Luas Jaminan</b>
<ul>
Jaminan Standar
<br>Pengangkutan Laut: Institute Cargo Clause (ICC) A, ICC B, ICC C
<br>Pengangkutan (Khusus) Darat: Cover A DAI, Cover B DAI
<br>Pengangkutan (Khusus) Udara: Institute Cargo Clause (Air)
</ul>
<b>Jaminan Perluasan</b>
<ul>
Perluasan jaminan dalam asuransi Marine Cargo, berasal dari:
<br>Pengecualian polis, sebagian pengecualian dalam polis dapat dimintakan sebagai perluasan dengan tambahan premi, seperti: Riot, Strike (Institute Strike Clause), War (Institute War Clause), dan lain-lain.
<br>Perluasan jaminan diluar pengecualian polis, umumnya dengan penggunaan klausula, seperti: Institute Replacement Clause, dan lain-lain
</ul>
Perbandingan Luas Jaminan Yang tercantum dalam ICC (Point A, B, dan C)
<ul>
<b>Catatan :</b>
Untuk barang baru (new goods) point (ICC A) ditanggung perusahaan asuransi.
<br>Untuk barang bekas (second goods) point (ICC B) saja, dan jika ada kerusakan seluruh atau sebagian hanya akan di proses perusahaan asuransi jika alat angkut (truck atau kapal) terlibat langsung terhadap kerusakan yang terjadi.
<br>Demikian juga untuk jenis barang second goods lainnya.
</ul>
<ul>
<b>Dengan diterbitkannya polis asuransi, pengirim dinyatakan telah memahami dan menyetujui ketentuan diatas.</b>
</ul>
