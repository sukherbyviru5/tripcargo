        <p style="color: black; text-align:left ;font-size: 1.2em; line-height:25px">
        <h1 style="background-color:#2196F3;height:auto; color: black; font-family: Arial, Helvetica, sans-serif; text-align:left ;font-size: 1.2em; line-height:25px; padding: 0px 0px 0px 10px;">
        <b>Trip Cargo jasa tranportasi darat untuk trucking dan armada sejenisnya untuk angkut barang besar.</b>
        </h1>
        
        <p style="color: black; text-align:left ;font-size: 1.2em; line-height:25px"> 
        Trip Cargo dalam operasional reguler menggunakan armada CCD (Colt Diesel Double)<br>
        dan ada beberapa jenis kendaraan untuk untuk mendukung kebutuhan angkutan barang antara lain:<br>
            
        <b>Truk long dengan ukuran medium hingga kapasitas besar atau panjang hingga 12 meter.</b><br>
        Kapasitas barang yang dimuata akan disesuaikan dengan kebutuhan armada yang akan digunakan, mempertimbangkan dengan berat barang atau panjangnnya. <br><br>
        
        
        <b>Armada Pick up dengang kapasitas dibawah 1 ton dengan kubikasi 3 - 4 kubik:</b><br>
        Kami sediakan dengan ukuran medimu seperti blinvan, pick up dengan daya angkut dikapasitas<br><br>
        
        <b>Truk CDE (Colt Diesel Engkel) yaitu jenis truk memiliki kubikasi 6 kubik:</b><br>
        Biasanya berguna untuk pengiriman barang banyak dan berat dengan kapasitas 2,5 ton.<br><br>
        
        <b>CCD (Colt Diesel Double):</b><br>
        Kapasitas 4 ton dengan volume 12 CBM (kubik)<br><br>
        
        <b>Fuso Bok atau Bak terbuka </b><br>
        Dengan kapasitas muatan 25 CBM (Kubik) atau 28-30 ton.<br><br>
        
        <b>Fuso Bak terbuk dengan kebutuhan extra panjang (Fuso long)</b><br>
        Fuso long dengan kapasitas muat barang yang banyak atau barang besar jumlah banyak serta barang seperti barang industri.<br><br>
        
        Seperti barang curah yang memerlukan penangan khusus dari semua ini mohon dikordinasikan terlebih dulu mempertimbangkan kemamanan barang dari sisi muatannya.<br>
        Untuk keamanan barang anda agar supaya tidak terjadi rusak atau over kapasitas di armada kami agar tidak dipaksakan dengan muatannya, 
        untuk memperlancar diperjalaan dan manufer armada dengan aman baik.<br><br>
        
