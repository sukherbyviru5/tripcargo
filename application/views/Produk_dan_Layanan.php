                 <h2 style="font-size: 1.4em;margin: unset;"><b>#Service Reguler</b></h2></a>   
                 Pelayanan pengiriman barang dengan waktu pengiriman normal barang akan sampai ke tujuan dalam jangka waktu yang standar dan tidak dipercepat,
                 dengan estimasi waktu yang diberikan oleh Trip Cargo untuk rute tertentu. <br>
                 Waktu pengiriman ini umumnya lebih lama dibandingkan layanan "ekspres" atau "kilat".<br>
                 Biaya untuk layanan reguler biasanya lebih murah dibandingkan layanan yang lebih cepat.<br>
                 Ini menjadikannya pilihan ideal jika Anda ingin menghemat biaya pengiriman.<br>
                 Layanan reguler cocok untuk pengiriman barang yang tidak memiliki batas waktu yang ketat atau urgensi tinggi.<br> 
                 Contohnya seperti kiriman barang dagangan, pindahan rumah tangga, atau barang pribadi lainnya yang tidak perlu buru-buru sampai.<br>
                 Trip Cargo menggunakan berbagai moda transportasi (darat, laut, atau udara).<br>
                 Ppelayanan pengiriman barang ke seluruh wilayah Indonesia dengan tarif perkilo gram dan standar pengiriman (door to door).<br>
                 <b>Tarif reguler</b> disesuaikan dengan jarak pengiriman ke tujuan baik diperjaalanan darat, laut atau via udara dengan standar layanaan reguler.<br><br>
                 
                 <a class="news-link orange" href="https://tripcargo.test/cargo" style="color: #1f1914 !important; ">
                 <h2 style="font-size: 1.4em;margin: unset;"><b>#Service Cargo</b></h2></a>   
                 Dalam kebutuhan pengiriman barang konsumen kami bisa memilih berdasarkan tarif yang cocok dan murah yaitu " <i><a href="https://tripcargo.test/web/Tarif_Kargo_Hemat"> <b>tarif cargo</b> </a></i>", lalu apa itu service cargo itu?.<br>
                 
                 Dari berbagi pilihan pelayanaan service cargo kami kelompokan ada beberapa kategori:<br> 
                 - Pertama dalam pengiriman dengan kapasitas banyak (barangnya ringan tapi besar) maka kami dihitung berdasarkan volume yang artinya (kubikasi).<br>
                 - Barangnya berat (tonase) tidak bisa diangkat dan membutuhkan atal batu (equipment), contoh besi baja yang gulungan maka kami hitung berdasarkan berat barangnya.<br>
                 <i><a style="color:blue; background-color: yellowgreen;">Service kargo pada umumnya ongkos kirim lebih murah serta estimasi perjalannan (waktu tempuh) akan lebih lama dengan perjalanannya via darat dan laut.</a></i>
                 Service cargo ada batas berat mimimal (contoh 30 kg) artinya dibawah berat 30 kg kami akan hitung minimum 30 kg lalu dikali tarif ongkos kirim perkilonya ke daerah tujuannya.
                 <br><br>
                 
                 
                 <h2 style="font-size: 1.4em;margin: unset;"><b>#Service Express</b></h2>   
                 Sanargo memiliki pelayanaan <a><i>Paket Service Express</i></a> dengan waktu kirim jadi cepat atau prioritas.<br>
                 Barang-barang urgent biasanya menggunakan service paket expess, barang tersebut mengedepankan kecepatan waktu kirim misal barang kami jemputan (pickup) lalu langsung pengantaran <b>(delivery faster)</b> bisa juga via udara (SMU)
                 dengan layanaan port to port atau door to port intinya barang tersebut cepat sampai.<br> 
                 Pelayanan ini paket cepat ini menjadi pilihan untuk kebutuhan urgent.
                 <br><br>
                 
                 <a class="news-link orange" href="https://tripcargo.test/Trucking" style="color: #1f1914 !important; "><h2 style="font-size: 1.4em;margin: unset;"><b>#Trucking</b></h2></a>   
                 Service trucking adalah layanan pengiriman barang menggunakan kendaraan truk atau kendaraan roda empat lainnya melalui jalur darat. <br>
        Ini adalah bagian penting dalam sektor logistik, menghubungkan berbagai lokasi, baik dalam kota maupun antar kota dan bahkan antar pulau.<br> 
        <b>Pengertian Trucking:</b><br>
        Trucking secara sederhana adalah proses pengangkutan barang dari satu tempat ke tempat lain menggunakan truk atau kendaraan angkutan darat lainnya.<br>
        Ini melibatkan berbagai jenis truk, mulai dari yang kecil untuk pengiriman ringan hingga truk besar untuk muatan lebih banyak.<br><br>
        Peran Trucking dalam Logistik:<br>
        <b>Pengiriman Barang:</b><br>
        Trucking adalah tulang punggung dalam pengiriman barang, memastikan barang sampai ke tujuan dengan aman dan tepat waktu.<br> 
        <b>Rantai Pasok:</b><br>
        Trucking memegang peranan penting dalam rantai pasokan, menghubungkan berbagai titik dalam proses distribusi barang.<br> 
        <b>Jangkauan Luas:</b><br>
        Layanan trucking dapat menjangkau berbagai lokasi, termasuk antar kota, antar pulau, dan bahkan pengiriman internasional.<br> 
        <b>Berbagai Jenis Barang:</b><br>
        Trucking dapat mengangkut berbagai jenis barang, mulai dari makanan, minuman, hingga barang-barang besar dan berat.<br> 
        <b>Efisiensi Biaya:</b><br>
        Dalam banyak kasus, trucking menawarkan solusi pengiriman yang lebih hemat biaya dibandingkan dengan moda transportasi lain seperti udara atau laut.<br><br> 
        
        Jenis-Jenis Trucking:<br>
        <b>Trucking Ekspedisi:</b><br>
        Layanan pengiriman barang yang lebih kecil dan seringkali menggabungkan beberapa pengiriman dalam satu truk.<br> 
        <b>Trucking Kargo:</b><br>
        Layanan pengiriman barang dalam jumlah besar, seringkali menggunakan truk trailer atau truk tronton.<br> 
        <b>Trucking Kontainer:</b><br>
        Pengiriman barang menggunakan peti kemas, biasanya untuk pengiriman jarak jauh atau internasional.<br><br>
        
        Keuntungan Menggunakan Layanan Trucking:<br>
        <b>Kapasitas Muatan Besar:</b><br>
        Truk dapat membawa barang dalam jumlah besar, sehingga cocok untuk pengiriman barang dalam volume besar.<br> 
        <b>Jangkauan Luas:</b><br>
        Layanan trucking dapat mencapai berbagai lokasi di dalam kota, antar kota, dan bahkan antar pulau.<br> 
        <b>Fleksibilitas:</b><br>
        Trucking dapat disesuaikan dengan berbagai jenis barang dan kebutuhan pengiriman.<br> 
        <b>Biaya Terjangkau:</b><br>
        Dibandingkan dengan moda transportasi lain, trucking seringkali menawarkan biaya yang lebih kompetitif.<br><br><br>
                 
                 
