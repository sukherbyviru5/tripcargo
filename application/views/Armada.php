<h1 style="text-align:left; border-bottom: groove; ">Armada operasional kami<div class='PCOnly'> CCD (Colt Diesel Double)</div></h1>
<div style="text-align:left ;margin-top:-40px; ">
Trip Cargo menyediakan beberapa jenis kendaraan untuk kebutuhan angkutan barang dari kebutuhan medium hingga kapasitas besar atau panjang hingga 12 meter.<br>
Kapasitas muatan armada tergantung pada jenis dan ukurannya. <br>
Berikut ini adalah beberapa jenis armada dan kapasitas muatannya:<br> 
Truk pickup: Kapasitas muatannya sekitar 1–1,5 ton.<br>
Truk engkel: Kapasitas muatannya sekitar 1,5–2,5 ton.<br>
Truk double engkel: Kapasitas muatannya sekitar 1,5–14 ton.<br>
Truk tronton: Kapasitas muatannya sekitar 15–25 ton.<br>
Truk wingbox: Kapasitas muatannya sekitar 10–20 ton.<br>
Truk trailer: Kapasitas muatannya sekitar 20–60 ton.<br>
Truk box: Kapasitas muatannya sekitar 4–10 ton.<br>
Truk fuso: Kapasitas muatannya sekitar 7–10 ton.<br>
Truk ringan: Kapasitas muatannya sekitar 1–5 ton.<br>
Truk sedang: Kapasitas muatannya sekitar 5–20 ton.<br>
Truk muatan berat: Kapasitas muatannya lebih dari 20 ton.<br>
Armada yang kami sediakan dirutinitas kami seperti blinvan, pick up dengan daya angkut dikapasitas dibawah 1 ton dengan kubikasi 3-4 kubik.<br>
Untuk CDE (Colt Diesel Engkel) yaitu jenis truk memiliki kubikasi 6 CBM (kubik) yang biasanya berguna untuk pengiriman barang banyak dan berat dengan kapasitas 2,5 ton.<br>
CCD (Colt Diesel Double) kapasitas 4 ton dengan volume 12 CBM (kubik) dan fuso kapasitas muatan 25 CBM (Kubik) atau 20 ton.<br>
Ada juga dengan kebutuhan extra panjang (long) jenis Fuso dari kapasitas muat barang yang banyak atau produk pabrikan / industri dengan jumlah banyak serta barang - barang industri 
atau barang curah yang memerlukan penangan khusus dari semua ini mohon dikordinasikan dengan kami demi kebutuhan kemamanan bersama dari sisi muatannya dan armada kami.<br>

Untuk keamanan barang anda agar supaya tidak terjadi rusak atau over melebihi dari armadanya dengan tidak dipaksa muatannya serta
untuk memperlancar diperjalaan dengan aman dari sisi pengguna dan driver serta armada kami, baik dengan armada box atau truk terbuka.<br> 

Untuk memesan armada dengan kebutuhan khusus misalkan seperti barang panjang dan lebah atau tinggi, barang besar, barang berat harus diinformasikan terlebih dahulu dengan detil ke kami, untuk kelanncaran pengerjaan<br>
kami untuk mengerjakan pengangkutan barang  atau bongkar muat barang memerlukan tambahan alat khusus misal forkip atau tatakan khusus atau menpower yang mencukupi misal barang curah, 
barang dipickup dari mana dan akan diantar ke derah mana begitu juga diperjalaan atau medan jalan bisa dilintasi armada tersebut atau tidak, harus selalu kordinasikan sebelumnya.<br>

Mudah-mudahan dengan penjelasan yang rinci ini untuk menghindari dari kerugian bersama.<br>
Dalam setiap pengiriman di Trip Cargo barang akan disurve terlebih dahulu atau dipastikan infofmasi panjang barang, berat barang, besar barang, gunan pertimbangan harga dan keamanan
usahakan dalam barang yang kami bawa dalam kemasan yang sempurna menghindari dari gesekan, lecet, pecah.<br>

Armada yang membawa barang kiriman selalu kami sesuaikan dengan kebutuhan kapasitasnya.<br> 
Dari daya kemampuan armada kami di ukur dari beban berat barang yang dimuat kedalam armada kami disarankan tidak melebihi dari ukuran yg disarankan diarmada tersebut.
